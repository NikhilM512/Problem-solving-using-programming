# Print P/F based on the input

n = int(input())  # Read an integer input from the user and store it in n

if n >= 40:  # Check if n is greater than or equal to 40
    print("P")  # If it is, print "P" (indicating a passing grade)
else:
    print("F")  # If n is not greater than or equal to 40, print "F" (indicating a failing grade)
